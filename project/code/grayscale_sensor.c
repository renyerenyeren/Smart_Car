#include "grayscale_sensor.h" 

// ============================ 全局变量定义区 ============================
/** 灰度传感器数据初始化 */
uint8 Gray_level = 0;                              // 灰度值初始化为0

// ============================ 函数定义区 ============================
/**
  * 函    数：Gray_Init
  * 功    能：灰度传感器初始化
  * 参    数：无
  * 返 回 值：无
  * 备    注：1. 配置传感器引脚为GPIO输入模式；
  *           2. 启用内部上拉电阻；
  *           3. 默认电平设置为低电平
  */
void Gray_Init(void)
{
    gpio_init(GRAY_SENSOR_PIN, GPI, GPIO_LOW, GPI_PULL_UP);
}

/**
  * 函    数：Gray_Get
  * 功    能：读取灰度传感器当前状态
  * 参    数：无
  * 返 回 值：灰度传感器电平状态（0或1）
  * 备    注：1. 返回值为数字量，0表示检测到黑线，1表示检测到白线；
  *           2. 具体电平逻辑需根据传感器实际特性确认
  */
uint8 Gray_Get(void)
{
    return gpio_get_level(GRAY_SENSOR_PIN);
}